package com.academic.useracademic.entities;

public class Curso {
    private Long id;
    private String name;
    public Curso() {

    }
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
